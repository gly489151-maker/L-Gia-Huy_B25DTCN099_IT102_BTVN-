#include <stdio.h>

int main(){
	int num1 = 10 ;
	int num2 = 20  ;
	
	// toan tu + 
	int tong = num1 + num2 ; 
	printf("phep tong la %d \n" , tong ) ;
	
	// toan tu -
	int hieu = num1-num2;
	printf("phep tong la  %d ", hieu); 

	// toan tu * 
	int tinh = num1 * num2 ;
	printf("phep nhan la %d ", tinh );
	
	
	// phep chia / 
	int thuong = num1 / num2 ;
	printf("phep chia / %d ", thuong );
	
	

	
	return 0 ;  
    
} 
